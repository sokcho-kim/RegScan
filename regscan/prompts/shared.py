# ═══════════════════════════════════════════════════════
# Builder — 블록 조합으로 시스템 프롬프트 조립
# ═══════════════════════════════════════════════════════

def build_system_prompt(
    today: str,
    *,
    include_reimbursement: bool = True,
    include_regulatory: bool = True,
    include_lifecycle: bool = True,
    extra_sections: list[str] | None = None,
) -> str:
    """공통 빌딩 블록을 조합하여 시스템 프롬프트를 생성한다.

    Parameters
    ----------
    today : str
        오늘 날짜 (YYYY-MM-DD). 시간추론 규칙의 {today} 자리에 주입됨.
    include_reimbursement : bool
        급여 도메인 지식 포함 여부.
    include_regulatory : bool
        규제 프로세스 도메인 지식 포함 여부.
    include_lifecycle : bool
        생애주기 분기 포함 여부.
    extra_sections : list[str] | None
        시스템별 추가 섹션 (drug-specific, stream-specific 등).
    """
    sections = [
        PERSONA,
        f"\n오늘 날짜: {today}\n",
        EXECUTIVE_TONE_RULES,
        TIME_REASONING_RULES.replace("{today}", today),
        ANTI_PATTERN_TABLE,
    ]

    if include_lifecycle:
        sections.append(LIFECYCLE_BRANCHES)
    if include_reimbursement:
        sections.append(DOMAIN_KNOWLEDGE_REIMBURSEMENT)
    if include_regulatory:
        sections.append(DOMAIN_KNOWLEDGE_REGULATORY)

    if extra_sections:
        sections.extend(extra_sections)

    sections.append(OUTPUT_FORMAT_RULES)

    return "\n\n".join(sections)
